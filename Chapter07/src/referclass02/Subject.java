package referclass02;

public class Subject {
	String subjectName;
	int Score;
	// getter, setter 생성
	public String getSubjectName() { // getter : 변수에 있는 값을 출력
		return subjectName;
	}
	public void setSubjectName(String subjectName) { // setter : 값을 세팅
		this.subjectName = subjectName;
	}
	public int getScore() {
		return Score;
	}
	public void setScore(int score) {
		Score = score;
	}
	
	
	
}
